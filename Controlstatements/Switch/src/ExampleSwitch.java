public class ExampleSwitch {
    public static void main(String args[]){
        int number=5;
        switch(number){
            case 1:
                System.out.println("likitha");

            case 2:
                System.out.println("tharal");

            case 4:
                System.out.println("priya");

            default:
                System.out.println("chocolate");
        }
    }
}
